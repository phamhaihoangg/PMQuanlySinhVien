    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       String regex_Phone = "^0[0-9]{9,10}$";
       String regex_Mail = "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";
        hocVien.setMa_hoc_vien(txtMSV.getText());
        hocVien.setTen_hoc_vien(txtTen.getText());
        hocVien.setSo_dien_thoai(txtPhone.getText());
        hocVien.setEmail(txtMail.getText());
        hocVien.setDia_chia(txtDiaChi.getText());
        Calendar calendar = dateChooserCombo1.getSelectedDate();
        LocalDate localDate = LocalDateTime.ofInstant(calendar.toInstant(), ZoneId.systemDefault()).toLocalDate();
        hocVien.setNgay_sinh(localDate);
        hocVien.setGioi_tinh(ckbGioiTinh.isSelected());
        hocVien.setTrang_thai(ckbTrangThai.isSelected());
        int selectLop = cboLopHoc.getSelectedIndex();
        hocVien.setLop_hoc_ID(lstHocVien.get(selectLop).getId());
        hocVien.setTen_lop_hoc(cboLopHoc.getSelectedItem().toString());
        if(txtMSV.getText().length()==0 || txtTen.getText().length() ==0 || txtPhone.getText().length() ==0 || txtMail.getText().length()== 0 || txtDiaChi.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin để tiến hành");
        }else if (txtPhone.getText().length() > 11 || txtPhone.getText().length() < 10 || regex_Phone.matches(txtPhone.getText())) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đúng số điện thoại để tiếp tục");
            }else if (regex_Mail.matches(txtMail.getText())){
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đúng email để tiếp tục");
            }else if (check_Mail_Phone(txtMail.getText(), txtPhone.getText())) {
                JOptionPane.showMessageDialog(null, "Số điện thoại hoặc Email đã được sử dụng!");
            } else {
        if (hocVien.getId() > 0) {
            
            hocVienDAO.update(hocVien);
        } else {
            hocVienDAO.insert(hocVien);
            JOptionPane.showMessageDialog(null, "Bạn đã thêm sinh viên mới thành công");
        }
        loadData();
    }//GEN-LAST:event_btnAddActionPerformed
    }
