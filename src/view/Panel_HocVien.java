        private boolean check_Mail_Phone(String mail, String phone) {
        boolean check = false;
        for (HocVien hv : lstHocVien) {
            if (hv.getSo_dien_thoai().equals(phone) || hv.getEmail().equals(mail)) {
                check = true;
            }
        }
        return check;
    }
    public void initData() {
        if (hocVien != null) {
            txtTen.setText(hocVien.getTen_hoc_vien() + "");
            txtPhone.setText(hocVien.getSo_dien_thoai());
            txtMail.setText(hocVien.getEmail());
            txtDiaChi.setText(hocVien.getDia_chia());
            dateChooserCombo1.setSelectedDate(DateTimeUtil.localDate2Calendar(hocVien.getNgay_sinh()));
            ckbGioiTinh.setSelected(hocVien.getGioi_tinh());
            cboLopHoc.setSelectedIndex((hocVien.getLop_hoc_ID()));

        } else {
            hocVien = new HocVien();
        }
    }

    private void showTable() {

//        jTable1.setCellSelectionEnabled(true);
        defaultTableModelHocVien = new DefaultTableModel();

        defaultTableModelHocVien.addColumn("Mã Sinh Viên");
        defaultTableModelHocVien.addColumn("Tên Sinh Viên");
        defaultTableModelHocVien.addColumn("Địa Chỉ");
        defaultTableModelHocVien.addColumn("Trạng Thái");
        defaultTableModelHocVien.addColumn("Lớp");
    }

    private void loadData() {
        lstHocVien = hocVienDAO.getAll();

        defaultTableModelHocVien.setRowCount(0);
        Vector v;
        for (HocVien hv : lstHocVien) {
            v = new Vector();
            v.add(hv.getMa_hoc_vien());
            v.add(hv.getTen_hoc_vien());
            v.add(hv.getDia_chia());
            v.add(hv.getTrang_thai() ? "Đang Học" : "Đã Tốt Nghiệp");
            v.add(hv.getTen_lop_hoc());

            defaultTableModelHocVien.addRow(v);
        }
        if (lstHocVien.size() > 0) {
            tblHocVien.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent lse) {
                    int pos = tblHocVien.getSelectedRow();
                    if (pos < 0) {
                        pos = 0;
                    }

                    hocVien = lstHocVien.get(pos);
                }
            });
        }
        tblHocVien.setModel(defaultTableModelHocVien);
    }

    void loadCboLop() {
        comboboxModelLopHoc = new DefaultComboBoxModel();
        lstLopHoc = lopHocDAO.getAll();
        for (LopHoc lopHoc : lstLopHoc) {
            comboboxModelLopHoc.addElement(lopHoc);
        }
        cboLopHoc.setModel(comboboxModelLopHoc);
    }

