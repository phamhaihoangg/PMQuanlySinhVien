    private void setMASV() {
        HocVienDAO hvd = new HocVienDAO();
        lstHocVien = hvd.getAll();
        int i = lstHocVien.size();
        if (i == 0) {
            txtMSV.setText("B" + String.format("%04d", 1));

        } else {
            String masv = lstHocVien.get(i - 1).getMa_hoc_vien().toString();
            masv = masv.substring(1);
            int id = (Integer.parseInt(masv)) + 1;
            txtMSV.setText("B" + String.format("%04d", id));
        }
    }

